#include <stdio.h>
/**
 * main - function is main
 * print if positive or negative or equal
 * Return: always zero
 */

int main(void)
{
	int n = 0;

	if (n == 0)
	{
		printf("is zero\n,");
	}
	else if (n > 0)
	{
		printf("is positive\n,");
	}
	else if (n < 0)
	{
		printf("is negative\n,");
	}
	return (0);
}
